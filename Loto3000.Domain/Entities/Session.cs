namespace Loto3000.Domain.Entities
{
    public class Session : IEntity
    {
        public Session() { }
        public Session(DateTime start, DateTime end)
        {
            Start = start;
            End = end;
        }
        public int Id { get; set; }
        public DateTime Start { get; set; } 
        public DateTime End { get; set; }
    }
}